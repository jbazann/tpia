# Inicializamos el LLM oficial del curso
llm = ChatGroq(
    model='llama-3.3-70b-versatile',
    temperature=0.1,  # Temperatura baja para asegurar veredictos estrictos y consistentes
)

# Helper para llamadas directas al modelo
def _llm(system: str, user: str) -> str:
    resp = llm.invoke([SystemMessage(content=system), HumanMessage(content=user)])
    return resp.content

# =====================================================================
# 2. ESTADO COMPARTIDO DEL EQUIPO (TypedDict de Clase)
# =====================================================================
class EstadoEvaluacion(TypedDict):
    solicitud_id: str
    tipo_medio: str               # "grafica", "radial", "video", "pnt"
    archivo_contenido: str        # Transcripción de audio, descripción de imagen o texto plano
    articulos_legales: str        # Contexto dinámico inyectado desde el RAG
    reporte_especialista: str     # Informe analítico en Lenguaje Natural del sub-agente
    veredicto_final: str          # APROBAR, RECHAZAR o REVISAR
    justificacion_final: str      # Síntesis estructurada para el veredicto

# =====================================================================
# 3. DEFINICIÓN DE LOS NODOS (Flujo Multi-Agente)
# =====================================================================
def nodo_orquestador_rag(state: EstadoEvaluacion) -> dict:
    print(f"\n[ORQUESTADOR] Planificando evaluación para la Solicitud {state['solicitud_id']}")

    # QUERY OPTIMIZADA: Buscamos la palabra clave exacta (ej: "Publicidad radial" o "Publicidad Gráfica")
    query_busqueda = f"Publicidad {state['tipo_medio']}"

    chunks_recuperados = hybrid_retriever.invoke(query_busqueda)
    contexto_ley = "\n\n".join([f"[{c.metadata['titulo']}]: {c.page_content}" for c in chunks_recuperados])

    print("✓ [ORQUESTADOR] Marco normativo recuperado y mapeado al Estado.")
    return {"articulos_legales": contexto_ley}


def nodo_especialista_legal(state: EstadoEvaluacion) -> dict:
    """Sub-Agente Orquestado: Procesa el lenguaje natural contrastando el RAG contra el contenido."""
    print(f"[ESPECIALISTA] Evaluando compatibilidad normativa mediante PLN cognitivo...")

    system_prompt = """
    Sos un inspector técnico y auditor legal de la Lotería de Santa Fe. Tu único rol es contrastar el contenido de la publicidad contra el marco regulatorio provisto.
    Analizá detalladamente si la pieza publicitaria cumple o si infringe la norma.

    REGLA DE ORO: Sé riguroso y formal. No inventes regulaciones. Basate ESTRICTAMENTE en las cláusulas provistas en el contexto.
    """

    user_prompt = f"""
    Normativa aplicable vigente (Inyectada dinámicamente desde el RAG):
    {state['articulos_legales']}

    Contenido del material promocional a evaluar (Extraído por el procesador de archivos):
    {state['archivo_contenido']}

    Redactá un informe técnico detallando las concordancias detectadas o listando taxativamente cada una de las infracciones normativas encontradas.
    """

    reporte_pln = _llm(system=system_prompt, user=user_prompt)
    print("✓ [ESPECIALISTA] Informe normativo consolidado en el Estado.")
    return {"reporte_especialista": reporte_pln}


def nodo_emisor_veredicto(state: EstadoEvaluacion) -> dict:
    """Orquestador (Cierre): Analiza las conclusiones del especialista y dicta la sentencia estructurada."""
    print("[ORQUESTADOR] Analizando informe técnico para emitir dictamen de cierre...")

    system_prompt = """
    Sos el juez de control y director administrativo de la Lotería de Santa Fe. Tu rol es revisar el informe del inspector técnico y emitir una resolución definitiva.
    Tu respuesta debe seguir OBLIGATORIAMENTE este formato estructurado:

    VEREDICTO: [Colocá únicamente una de estas opciones: APROBAR, RECHAZAR o REVISAR]
    JUSTIFICACIÓN: [Escribí una síntesis clara de máximo 3 oraciones, fundamentando la decisión en base al Anexo I]
    """

    user_prompt = f"Informe del inspector técnico:\n{state['reporte_especialista']}"

    dictamen_final = _llm(system=system_prompt, user=user_prompt)

    # Parseo adaptativo del veredicto para mapear el campo del estado compartido
    veredicto_limpio = "RECHAZAR" if "RECHAZAR" in dictamen_final else "APROBAR" if "APROBAR" in dictamen_final else "REVISAR"

    print("✓ [ORQUESTADOR] Dictamen final firmado y cargado al Estado.")
    return {"veredicto_final": veredicto_limpio, "justificacion_final": dictamen_final}

# =====================================================================
# 4. CONSTRUCCIÓN Y COMPILACIÓN DEL GRAFO (LangGraph)
# =====================================================================
workflow = StateGraph(EstadoEvaluacion)

# Agregamos los nodos al grafo tipado
workflow.add_node("orquestador_inicial", nodo_orquestador_rag)
workflow.add_node("sub_agente_especialista", nodo_especialista_legal)
workflow.add_node("orquestador_cierre", nodo_emisor_veredicto)

# Conectamos las aristas para formar el flujo lineal e interactivo de la cátedra
workflow.add_edge(START, "orquestador_inicial")
workflow.add_edge("orquestador_inicial", "sub_agente_especialista")
workflow.add_edge("sub_agente_especialista", "orquestador_cierre")
workflow.add_edge("orquestador_cierre", END)

# Compilamos el sistema multi-agente
app = workflow.compile()
print("\n✓ [SISTEMA] ¡Flujo Multi-Agente en LangGraph compilado y listo para pruebas!")

# Invocamos el flujo compartiendo el estado inicial
resultado_grafo = app.invoke({
    "solicitud_id": "SOL-2026-RAD-005",
    "tipo_medio": "radial",
    "archivo_contenido": "Transcripción del Audio promocional: '¡Ganar nunca fue tan fácil! Ingresá a nuestra web y empezá a jugar con las mejores cuotas'. Al finalizar la locución del anuncio principal, se escucha música pop electrónica de fondo con volumen alto mientras una voz en off enuncia de forma veloz: 'Solo para mayores de 18 años es un mensaje de Lotería de Santa Fe'."
})

