# ==========================================
# 2. CHUNKING CONTROLADO POR PUNTOS NORMATIVOS
# ==========================================
# Separamos limpiamente usando los identificadores numéricos del documento real
parts = re.split(r'(?=\n\s*\d+\.\s+)', full_text)
parts = [p.strip() for p in parts if p.strip()]

docs = []
for p in parts:
    # Extraemos el número y la primera línea como título semántico
    lines = p.split('\n')
    header_line = lines[0]

    match_num = re.match(r'^(\d+)\.', header_line)
    art_num = int(match_num.group(1)) if match_num else 0

    docs.append(Document(
        page_content=p,
        metadata={'art': art_num, 'titulo': header_line}
    ))

print(f"✓ {len(docs)} puntos normativos procesados y chunkedos.")

# ==========================================
# 3. GENERACIÓN DE EMBEDDINGS E INDEXACIÓN
# ==========================================
model_name = 'paraphrase-multilingual-MiniLM-L12-v2'
emb = HuggingFaceEmbeddings(model_name=model_name)

# Guardamos en ChromaDB local
vector_store = Chroma.from_documents(docs, emb, collection_name='rve_303_26')
print(f"✓ Chunks indexados con éxito en ChromaDB.")

# =====================================================================
# 4. CONFIGURACIÓN DEL PIPELINE HÍBRIDO (Corregido con k=3)
# =====================================================================
# Subimos k a 3 para garantizar que el LLM reciba suficiente contexto normativo
dense_retriever = vector_store.as_retriever(search_kwargs={'k': 3})

bm25_retriever = BM25Retriever.from_documents(docs)
# Subimos k a 3 también en el recuperador léxico BM25
bm25_retriever.k = 3

# Fusión híbrida estable
hybrid_retriever = EnsembleRetriever(
    retrievers=[bm25_retriever, dense_retriever],
    weights=[0.5, 0.5]
)
print("✓ Pipeline Híbrido optimizado con k=3 para evitar pérdidas de contexto.")

